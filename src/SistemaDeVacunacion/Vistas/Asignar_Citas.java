private void armarCabecera(){
            ArrayList<Object> filaCabecera = new ArrayList<>();
            filaCabecera.add("ID");
            filaCabecera.add("DNI");
            filaCabecera.add("Patologia");
            filaCabecera.add("ambitoTrabajo");
            for( Object it: filaCabecera) {
                modelo.addColumn(it);
            }
            jTPrioridad.setModel(modelo);
        }

