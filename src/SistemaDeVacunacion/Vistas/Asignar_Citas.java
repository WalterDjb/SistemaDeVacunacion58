private void cargaDatos() {
            
            List<Cita> lista = ciData.buscarPersonasSinTurno();
            for (Cita a: lista) {
                modelo.addRow(new Object[]{a.getId(),a.getDni(),a.getCiudadano().getPatologia(),a.getCiudadano().getAmbito()});
            }
        }
